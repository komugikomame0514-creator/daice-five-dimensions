let currentKey="terminal";
const cards=[...document.querySelectorAll(".tour-card")];
const detail=document.getElementById("liveDetail");
const esc=v=>String(v??"").replace(/[&<>"']/g,m=>({"&":"&amp;","<":"&lt;",">":"&gt;",'"':"&quot;","'":"&#039;"}[m]));

function renderTour(key,scroll=true){
 const t=tours.find(x=>x.key===key)||tours[0]; currentKey=t.key;
 document.documentElement.style.setProperty("--live-accent",t.accent);
 detail.style.setProperty("--accent",t.accent); detail.style.setProperty("--detail-poster",t.detailPoster);
 detailType.textContent=t.type; detailTitle.textContent=t.title; detailSubtitle.textContent=t.subtitle;
 detailStory.textContent=t.story; detailCopy.textContent=t.copy;
 factScale.textContent=t.scale; factPeriod.textContent=t.period; factStatus.textContent=t.status;
 factMedia.textContent=t.media; factCities.textContent=t.cities; factShows.textContent=t.shows;
 officialTour.href=t.official; verifiedBadge.hidden=!t.verified;
 conceptTags.innerHTML=(t.concept||[]).map(x=>`<span>${esc(x)}</span>`).join("");
 highlightList.innerHTML=(t.highlights||[]).map((x,i)=>`<li><span>${String(i+1).padStart(2,"0")}</span><strong>${esc(x)}</strong></li>`).join("");
 setlistFlow.innerHTML=(t.phases||[]).map((x,i)=>`<li><strong>${String(i+1).padStart(2,"0")} / ${esc(x)}</strong><span>LIVE EXPERIENCE MAP — 世界観をたどる編集構成</span></li>`).join("");
 const schedule=t.schedule||[]; schedulePanel.hidden=!schedule.length; scheduleCount.textContent=schedule.length?`${schedule.length} PERFORMANCES`:"";
 scheduleList.innerHTML=schedule.map((r,i)=>`<div class="schedule-row"><span class="schedule-no">${String(i+1).padStart(2,"0")}</span><time>${esc(r[0])}</time><span class="schedule-area">${esc(r[1])}</span><strong>${esc(r[2])}</strong><span class="schedule-time">OPEN ${esc(r[3])} / START ${esc(r[4])}</span></div>`).join("");
 releasePanel.hidden=!t.releaseTitle; releaseTitle.textContent=t.releaseTitle||""; releaseDate.textContent=t.releaseDate||""; releaseCopy.textContent=t.releaseCopy||""; releaseLink.href=t.releaseLink||t.official;
 const priceData=t.prices||[]; pricePanel.hidden=!priceData.length; priceList.innerHTML=priceData.map(x=>`<li>${esc(x)}</li>`).join("");
 cards.forEach(c=>c.classList.toggle("active",c.dataset.tour===key));
 if(scroll) detail.scrollIntoView({behavior:"smooth",block:"start"});
}
cards.forEach(card=>card.addEventListener("click",()=>renderTour(card.dataset.tour)));
